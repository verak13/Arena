	@Autowired
	EventRepository eventRepository;

	@Autowired
	SeatService seatService;

	@Transactional(readOnly = false, propagation = Propagation.REQUIRED)
	public Event addEvent(EventDTO dto) {
		if (dto.getEndDate().before(dto.getStartDate()) || dto.getDeadline().after(dto.getStartDate())) {
			return null;
		}
		if (eventRepository.checkEvents(dto.getStartDate(), dto.getEndDate()) > 0L) {
			return null;
		}
		return eventRepository.save(eventMapper.toEntity(dto, seatNames));
	}

	@Transactional(readOnly = false, propagation = Propagation.REQUIRED)
	public Event updateEvent(EventDTO dto) {
		try {
			Event e = eventRepository.findById(dto.getId()).orElseThrow(RuntimeException::new);
			e.setDeadline(dto.getDeadline());
			e.setDescription(dto.getDescription());
			e.setEndDate(dto.getEndDate());
			e.setParterPrice(dto.getParterPrice());
			e.setEastPrice(dto.getEastPrice());
			e.setWestPrice(dto.getWestPrice());
			e.setNorthPrice(dto.getNorthPrice());
			e.setSouthPrice(dto.getSouthPrice());
			e.setVipPrice(dto.getVipPrice());
			e.setStartDate(dto.getStartDate());
			e.setName(dto.getName());
			return eventRepository.save(e);
		} catch (RuntimeException re) {
			re.printStackTrace();
			return null;
		}

	}

	@Transactional(readOnly = false, propagation = Propagation.REQUIRED)
	public Boolean deleteEvent(Long id) {
		try {
			Event e = eventRepository.findById(id).orElseThrow(Exception::new);
			return seatService.deleteSeats(e.getSeats());
		} catch (Exception ex) {
			ex.printStackTrace();
			return false;
		}

	}

	@Transactional(readOnly = true)
	public Page<EventDTO> getEvents(Pageable pageable) {
		Page<Event> page = eventRepository.findAll(pageable);
		List<EventDTO> dtos = eventMapper.toEventDTOList(page.toList());
		return new PageImpl<>(dtos, page.getPageable(), page.getTotalElements());
	}

	@Transactional(readOnly = true)
	public EventResponseDTO getEvent(Long id, String email) {
		Event e = eventRepository.getById(id);
		EventResponseDTO dto = eventMapper.toDtoWithSeats(e);
		List<SeatDTO> seatsDTO = new ArrayList<SeatDTO>();
		List<Seat> seatsList = seatService.getByEvent(e.getId());
		List<String> mySeats = seatService.getMySeats(e.getId(), email);
		// Collections.sort(seatsList);
		for (Seat seat : seatsList) {
			Boolean occupiedByMe = mySeats.contains(seat.getSeatName());
			SeatDTO sDTO = new SeatDTO(seat.getId(), seat.getSeatName(), seat.getVersion(), seat.getOccupied(),
					occupiedByMe);
			seatsDTO.add(sDTO);
		}
		dto.setSeats(seatsDTO);
		return dto;
	}

	public Event getById(Long id) {
		return eventRepository.getById(id);
	}

